package com.example.and3lesson6;


public class Math {

    public int add(int a, int b) {
        return a + b;
    }

    public int multiply(int a, int b) {
        return a * b;
    }

    public int div(int a, int b) {
        return a / b;
    }

    public int minus(int a, int b) {
        return a - b;
    }

    public int sqrt(int a) {
        return a * a;
    }
}
